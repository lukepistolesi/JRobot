package robotCli;

public class Playground {

	private int width, height;

	public Playground(int width, int height) {
    this.setWidth(width);
    this.setHeight(height);
  }

  public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}

	public int getHeight() {
		return height;
	}

	public void setHeight(int height) {
		this.height = height;
	}

}
